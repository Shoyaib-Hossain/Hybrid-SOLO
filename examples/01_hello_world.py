#!/usr/bin/env python3
"""
Example 1: Hello World
======================

This is your first Python program!
It demonstrates:
- Print statements
- String formatting
- Variables
"""

# Simple print statement
print("Hello, World!")

# Using variables
name = "Alice"
print(f"Hello, {name}!")

# Getting user input
user_name = input("What's your name? ")
print(f"Nice to meet you, {user_name}!")

# Multiple variables
greeting = "Hello"
punctuation = "!"
print(f"{greeting}, {user_name}{punctuation}")

